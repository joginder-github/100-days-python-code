print("line a \\n line b")  # line a \n line b
print("\\\"\\\'")           # \"\'
print("this is \\\\ double backslash")  # this is \\ double backslash
print("these are /\\/\\/\\/\\/\\ mountains")    # these are /\/\/\/\/\ mountains
print("he is\tawesome") # he is   awesome
print("\\\" \\n \\t \\\'")  # \" \n \t \'
print(r"\" \n \t \'")   # \" \n \t \'