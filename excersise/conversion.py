dec = int(input("Enter any decimal number="))

print("The decimal value of",dec,"is:")
print(bin(dec),"in binary.")
print(oct(dec),"in octal.")
print(hex(dec),"in hexadecimal.")

# output:

# Enter any decimal number=26
# The decimal value of 26 is:
# 0b11010 in binary.
# 0o32 in octal.
# 0x1a in hexadecimal.